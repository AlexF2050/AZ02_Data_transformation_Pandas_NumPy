import pandas as pd

data = {
    'набор А':[85, 90, 95, 100, 105],
    'набор В':[70, 80, 95, 110, 120],
}

df = pd.DataFrame(data) #стандартный набор данных

stdA = df['набор А'].std() #стандартное отклонение 1 набора
stdB = df['набор В'].std() #стандартное отклонение 2 набора

print(f'Стандартное отклонение 1 набор: {stdA}')
print(f'Стандартное отклонение 2 набор: {stdB}')